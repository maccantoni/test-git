# Usamos la función "print" para mostrar un mensaje en pantalla.

print("Hola! Vamos a aprender como se usa la función print e input")

# Usamos la función input para pedirle al usuario que ingrese cierta información.
# El texto dentro de los paréntesis es lo que se le muestra al usuario.

# Para guardar esta información, lo hago usando una variable a la cual le doy un nombre (nombre) y se le asigna un valor que en este caso,
# estará dado por lo que ingrese el user en el input.

nombre = input("Hola! Ingresa tu nombre: ")

# Ahora usamos print nuevamente para mostrar en pantalla el dato ingresado por el user.

print("¡Mucho gusto, " +  nombre + "!")

# Ejemplo extra: pedimos la edad al usuario

edad = input("¿Cuantos años tienes? ")

# Usamos nuevamente print para mostrar el resultado.

print("¡Wow, " + edad + ", estás lista para comenzar!")

# la información del input se almacena como cadena de caracteres.
